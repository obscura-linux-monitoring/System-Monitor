#pragma once

/**
 * @file collector.h
 * @brief 시스템 모니터링을 위한 기본 수집기 클래스 정의
 * @author 시스템 모니터링 개발팀
 *
 * 시스템 모니터링 애플리케이션에서 사용되는 모든 데이터 수집기의
 * 기본 인터페이스를 정의하는 추상 클래스입니다.
 */

/**
 * @class Collector
 * @brief 시스템 모니터링을 위한 데이터 수집기의 기본 추상 클래스
 *
 * 다양한 시스템 리소스(CPU, 메모리, 디스크, 네트워크 등)에 대한
 * 데이터를 수집하는 다양한 수집기의 공통 인터페이스를 정의합니다.
 * 이 클래스를 상속받아 구체적인 수집 기능을 구현합니다.
 *
 * 사용 예시:
 * @code
 * class CPUCollector : public Collector {
 * public:
 *     void collect() override {
 *         // CPU 정보 수집 구현
 *     }
 * };
 * @endcode
 */
class Collector
{
public:
    /**
     * @brief 수집기의 데이터 수집 작업을 정의하는 순수 가상 함수
     *
     * 이 함수는 수집기가 구현해야 하는 핵심 기능을 정의합니다.
     * 각 구현체는 이 메서드를 오버라이드하여 특정 시스템 리소스
     * (CPU, 메모리, 디스크, 네트워크, 프로세스 등)에 대한 데이터 수집
     * 로직을 구현해야 합니다.
     *
     * @note 이 함수는 요청 시 최신 데이터를 수집해야 하며,
     *       시스템 부하를 최소화하도록 구현되어야 합니다.
     */
    virtual void collect() = 0;

    /**
     * @brief 수집기 소멸자
     *
     * 가상 소멸자를 통해 상속 계층에서 적절한 소멸이 이루어지도록 합니다.
     * 하위 클래스에서 할당한 자원을 정리하는 로직을 구현할 수 있도록 합니다.
     */
    virtual ~Collector() = default;
};